/** キャラclug に対応する考え中リアクションをランダムに1つ返す */
export function getThinkingReaction(characterSlug: string): string {
  const reactions = THINKING_REACTIONS[characterSlug] ?? DEFAULT_REACTIONS;
  return reactions[Math.floor(Math.random() * reactions.length)];
}
