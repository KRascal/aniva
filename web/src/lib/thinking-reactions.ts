/**
 * キャラ固有の「考え中」テキストをランダムに返す
 * @param slug キャラクターのslug
 * @returns 考え中メッセージ
 */
export function getThinkingReaction(slug: string): string {
  const reactions = THINKING_REACTIONS[slug] ?? GENERIC_REACTIONS;
  return reactions[Math.floor(Math.random() * reactions.length)];
}
