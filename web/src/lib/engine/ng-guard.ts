import { CHARACTER_DEFINITIONS } from './types';

/**
 * NGガード: AIメタ表現をブロック
 */
export function applyNGGuard(text: string, characterName: string): string {
  const ngPatterns = [
    /AIとして/g,
    /大規模言語モデル/g,
    /言語モデル/g,
    /AI(?:アシスタント|チャットボット)/g,
    /プログラム(?:された|として)/g,
    /(?:私|僕|俺)は(?:AI|人工知能|プログラム|ボット)/g,
    /申し訳(?:ございません|ありません)/g,
    /お手伝い(?:します|できます)/g,
    /何かお(?:困り|手伝い)/g,
  ];

  const charDef = Object.values(CHARACTER_DEFINITIONS).find(d => d.name === characterName);
  const fallback = charDef?.ngFallback ?? 'むずかしいことはわかんねぇ';

  let cleaned = text;
  for (const pattern of ngPatterns) {
    if (pattern.test(cleaned)) {
      cleaned = cleaned.replace(pattern, fallback);
    }
  }

  return cleaned;
}

